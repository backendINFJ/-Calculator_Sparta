package carculator;

public class DivideOperation { // 나눗셈 메서드

    public static double div(double num1, double num2) {

        double result; // 연산값

        result = num1 / num2;

        return  result; // 연산값 반환
    }

    public static double div2(double num1, double num2) { // 나머지 메소드

        double result; // 연산값

        result = num1 % num2;

        return  result; // 연산값 반환
    }
}
